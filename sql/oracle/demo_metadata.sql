CREATE TABLE DEPARTMENT (
    DEPARTMENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    DEPARTMENT_CODE VARCHAR2(20) NOT NULL,
    DEPARTMENT_NAME VARCHAR2(120) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_DEPARTMENT PRIMARY KEY (DEPARTMENT_ID),
    CONSTRAINT UQ_DEPARTMENT_CODE UNIQUE (DEPARTMENT_CODE)
);

CREATE TABLE EMPLOYEE (
    EMPLOYEE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    DEPARTMENT_ID NUMBER NOT NULL,
    EMPLOYEE_CODE VARCHAR2(30) NOT NULL,
    FULL_NAME VARCHAR2(150) NOT NULL,
    SALARY NUMBER(12,2),
    HIRED_AT DATE DEFAULT SYSDATE NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_EMPLOYEE PRIMARY KEY (EMPLOYEE_ID),
    CONSTRAINT UQ_EMPLOYEE_CODE UNIQUE (EMPLOYEE_CODE),
    CONSTRAINT FK_EMPLOYEE_DEPARTMENT FOREIGN KEY (DEPARTMENT_ID)
        REFERENCES DEPARTMENT (DEPARTMENT_ID)
);

CREATE TABLE EMPLOYEE_AUDIT (
    AUDIT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    EMPLOYEE_ID NUMBER NOT NULL,
    ACTION_TYPE VARCHAR2(20) NOT NULL,
    ACTION_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_EMPLOYEE_AUDIT PRIMARY KEY (AUDIT_ID),
    CONSTRAINT FK_EMPLOYEE_AUDIT_EMPLOYEE FOREIGN KEY (EMPLOYEE_ID)
        REFERENCES EMPLOYEE (EMPLOYEE_ID)
);

CREATE INDEX IDX_EMPLOYEE_DEPARTMENT_ID ON EMPLOYEE (DEPARTMENT_ID);
CREATE INDEX IDX_EMPLOYEE_AUDIT_EMPLOYEE_ID ON EMPLOYEE_AUDIT (EMPLOYEE_ID);

